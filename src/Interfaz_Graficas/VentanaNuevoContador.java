    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            Contador c = new Contador();

            c.setRut(this.txtRut.getText().toString());
            c.setNombre(this.txtNombres.getText().toString());
            c.setEdad(Integer.parseInt(this.txtEdad.getText()));
            c.setDireccion(this.txtDireccion.getText().toString());
            c.setContraseña(this.txtContraseña.getText());
            c.setComuna(this.txtComuna.getText().toString());
            n.IngresarCliente(c);
            n.IngresarUsuario(this.txtRut.getText().toString(), this.txtContraseña.getText());

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "*Porfavor verifique que todos los campos esten llenos \n **Que el cliente ya no este ingresado");
        }
        JOptionPane.showMessageDialog(this, "Cliente Agregado con Exito", "Cliente Agregado", JOptionPane.INFORMATION_MESSAGE);
       Limpiar ();

    }//GEN-LAST:event_btnGuardarActionPerformed

