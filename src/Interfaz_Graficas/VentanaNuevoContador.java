    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        try {
            Contador c = n.BuscarCliente(this.txtRut.getText().toString());
            if (c.getEdad() == 0) {
                JOptionPane.showMessageDialog(null, "Rut no existe en la base de datos");
            } else {
                this.txtNombres.setText(c.getNombre());
                this.txtEdad.setText("" + c.getEdad());
                this.txtDireccion.setText(c.getDireccion());
                this.txtContraseña.setText(c.getContraseña());
                this.txtComuna.setText(c.getComuna());
                this.txtRut.setEditable(false);
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

